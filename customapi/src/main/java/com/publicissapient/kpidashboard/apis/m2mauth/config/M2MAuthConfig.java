package com.publicissapient.kpidashboard.apis.m2mauth.config;

import jakarta.validation.constraints.NotBlank;
import jakarta.validation.constraints.NotNull;
import lombok.Data;
import org.springframework.boot.autoconfigure.condition.ConditionalOnProperty;
import org.springframework.boot.context.properties.ConfigurationProperties;
import org.springframework.context.annotation.Configuration;

@Data
@Configuration
@ConfigurationProperties(prefix = "m2mauth")
@ConditionalOnProperty(prefix = "m2mauth", name = "issuer-service-id")
public class M2MAuthConfig {
    @NotBlank
    private String issuerServiceId;

    @NotBlank
    private String secret;

    @NotNull
    Integer duration;
}
